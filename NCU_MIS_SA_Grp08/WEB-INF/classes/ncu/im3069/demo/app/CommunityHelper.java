    /** 建立該社群至資料庫 */
    public JSONObject create(Community c) {
    	/** 記錄實際執行之SQL指令 */
        String exexcute_sql = "";
        /** 紀錄程式開始執行時間 */
        long start_time = System.nanoTime();
        /** 紀錄SQL總行數 */
        int row = 0;
        int rsID=-1;
        ResultSet rs=null;
        try {
            /** 取得資料庫之連線 */
            conn = DBMgr.getConnection();
            /** SQL指令 */
            String sql = "INSERT INTO `missa`.`community`(`name`, `content`, `total_member`)"
                    + " VALUES(?, ?, ?)";
            
            /** 取得所需之參數 */
            String name = c.getName();
            String content = c.getContent();
            int total_member=c.getTotal_member();
            
            /** 將參數回填至SQL指令當中 */
            pres = conn.prepareStatement(sql,Statement.RETURN_GENERATED_KEYS);
            pres.setString(1, name);
            pres.setString(2, content);
            pres.setInt(3, total_member);
            
            
            
            /** 執行新增之SQL指令並記錄影響之行數 */
            row = pres.executeUpdate();
            rs = pres.getGeneratedKeys();
            if(rs != null && rs.next()){
                rsID=rs.getInt(1);
            }
            /** 紀錄真實執行的SQL指令，並印出 **/
            exexcute_sql = pres.toString();
            System.out.println(exexcute_sql);
            

        } catch (SQLException e) {
            /** 印出JDBC SQL指令錯誤 **/
            System.err.format("SQL State: %s\n%s\n%s", e.getErrorCode(), e.getSQLState(), e.getMessage());
        } catch (Exception e) {
            /** 若錯誤則印出錯誤訊息 */
            e.printStackTrace();
        } finally {
            /** 關閉連線並釋放所有資料庫相關之資源 **/
            DBMgr.close(pres, conn);
        }

        /** 紀錄程式結束執行時間 */
        long end_time = System.nanoTime();
        /** 紀錄程式執行時間 */
        long duration = (end_time - start_time);

        /** 將SQL指令、花費時間與影響行數，封裝成JSONObject回傳 */
        JSONObject response = new JSONObject();
        response.put("sql", exexcute_sql);
        response.put("time", duration);
        response.put("row", row);
        response.put("rsID", rsID);
        return response;
    }

